//! All documented registers from the ADXL345 datasheet.
#![expect(dead_code, reason = "Not all register values are used")]

macro_rules! register {
    ($($ident:ident: $addr:expr,)+) => {
        $(pub(super) const $ident: u8 = $addr;)+
    };
}

